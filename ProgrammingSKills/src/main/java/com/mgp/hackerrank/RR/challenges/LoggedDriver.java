/*  Class gives information about the driver */
public class LoggedDriver {

	private double driverId;
	private double loginTime;
	private double logoutTime;
	private double totalLoggedTime;
	private double totalLoggedTimeDuringPeak;
	/**
	 * @return the driverId
	 */
	public double getDriverId() {
		return driverId;
	}
	/**
	 * @param driverId the driverId to set
	 */
	public void setDriverId(double driverId) {
		this.driverId = driverId;
	}
	/**
	 * @return the loginTime
	 */
	public double getLoginTime() {
		return loginTime;
	}
	/**
	 * @param loginTime the loginTime to set
	 */
	public void setLoginTime(double loginTime) {
		this.loginTime = loginTime;
	}
	/**
	 * @return the logoutTime
	 */
	public double getLogoutTime() {
		return logoutTime;
	}
	/**
	 * @param logoutTime the logoutTime to set
	 */
	public void setLogoutTime(double logoutTime) {
		this.logoutTime = logoutTime;
	}
	/**
	 * @return the totalLoggedTime
	 */
	public double getTotalLoggedTime() {
		return totalLoggedTime;
	}
	/**
	 * @param totalLoggedTime the totalLoggedTime to set
	 */
	public void setTotalLoggedTime(double totalLoggedTime) {
		this.totalLoggedTime = totalLoggedTime;
	}
	/**
	 * @return the totalLoggedTimeDuringPeak
	 */
	public double getTotalLoggedTimeDuringPeak() {
		return totalLoggedTimeDuringPeak;
	}
	/**
	 * @param totalLoggedTimeDuringPeak the totalLoggedTimeDuringPeak to set
	 */
	public void setTotalLoggedTimeDuringPeak(double totalLoggedTimeDuringPeak) {
		this.totalLoggedTimeDuringPeak = totalLoggedTimeDuringPeak;
	}
	
	
}
